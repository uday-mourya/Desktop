package com.model;

/**
 *
 * @author hp
 */

public class Account {
    private int id ;
    private double balance;
    private String number, type ,name,status,bank,branch;

  
    public Account() {
    }

    public Account(int id, double balance, String number, String type, String name, String status, String bank, String branch) {
        this.id = id;
        this.balance = balance;
        this.number = number;
        this.type = type;
        this.name = name;
        this.status = status;
        this.bank = bank;
        this.branch = branch;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getBalance() {
        return balance;
    }

    public void setBalance(double balance) {
        this.balance = balance;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getBank() {
        return bank;
    }

    public void setBank(String bank) {
        this.bank = bank;
    }

    public String getBranch() {
        return branch;
    }

    public void setBranch(String branch) {
        this.branch = branch;
    }
 
}
